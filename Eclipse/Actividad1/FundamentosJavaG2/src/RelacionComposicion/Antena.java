package RelacionComposicion;

public class Antena {
	private int potencia;

	/**
	 * @param potencia
	 */
	public Antena(int potencia) {
		super();
		this.potencia = potencia;
	}

	/**
	 * @return the potencia
	 */
	public int getPotencia() {
		return potencia;
	}

	/**
	 * @param potencia the potencia to set
	 */
	public void setPotencia(int potencia) {
		this.potencia = potencia;
	}
	
	

}
